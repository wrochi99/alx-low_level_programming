#include "main.h"

/**
 * _print_rev_recursion - print string string in reverse
 *
 * @s: string to be reversed
 *
 * Return: void
 */
void _print_rev_recursion(char *s)
{
	int i;
	char s[i];

	if (i == 0)
	{
		_putchar('\n');
	}

	while (i > 0)
	{
		i++;
		if (i == '\0')
		{
			break;
		}
		_putchar(s[i]);
		i--;
	}
	_putchar('\n');
}
